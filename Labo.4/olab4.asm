;PROCEDIMIENTO QUE LIMPIA LA PANTALLA
LIMPIAR	PROC NEAR
	MOV AX, 0600H	; peticion de recorrido de pantalla
	MOV BH, 0		; color 
	MOV CX, 0000	; de 00,00
	MOV DX, 184FH	; A 24, 79
	INT 10H
	RET
LIMPIAR	ENDP
;------------------------------------------------------------------------------

;PROCEDIMIENTO QUE GENERA UN DELAY 
DELAY PROC NEAR
MOV AH, 86H
	MOV CX, 15
	MOV DX, 5000
	INT 15H
RET
DELAY ENDP

; ------------------- Programa principal --------------------
MAIN PROC
	.STARTUP
	MOV     AX,@DATA		; INICIAR REGISTROS DE SEGMENTO
    MOV     DS, AX

	MOV		AX, VIDEO_SEG
	MOV 	ES, AX
	ASSUME	ES:VIDEO_SEG
	AND BX, 00	
	MOV SI, 00
	MOV		AX, 0013H		; Modo de video
	INT		10H	

REPETIR:
	CALL LIMPIAR

	MOV AL, VAR1
	CMP AL, 0
	JE DIBUJO1
	CMP AL, 1
	JE DIBUJO2
	CMP AL, 2
	JE DIBUJO3
	JMP CONTINUAR
	
	DIBUJO1:
		CALL DIBUJAR3
		JMP CONTINUAR
		
	DIBUJO2:
		CALL DIBUJAR1
		JMP CONTINUAR
	
	DIBUJO3:
		CALL DIBUJAR2
		JMP CONTINUAR
		
CONTINUAR:
	CALL TECLA
	
	CMP AL, ASCII_A		
	JE MOVER1
	CMP AL, ASCII_S
	JE MOVER2
	CMP AL, ASCII_D
	JE MOVER3
	CMP AL, 27
	JE	SALIR
	JMP REPETIR

MOVER1:
	XOR AL, AL
	MOV AL, 1
	MOV VAR1, AL
	JMP SEGUIR

MOVER2: 
	XOR AL, AL
	MOV AL, 2
	MOV VAR1, AL
	JMP SEGUIR
	
MOVER3: 
	XOR AL, AL
	MOV VAR1, AL
	JMP SEGUIR

SEGUIR:
	XOR AL, AL
	MOV AX, XORIG
	ADD AX, 5
	MOV XORIG, AX
	JMP REPETIR
	
	
	
SALIR:
	CALL RESTAURAR_MODO_ORIGINAL
	MOV AX,4C00H					; salida a DOS
	INT 21H		    
MAIN ENDP	

; ------------------- Obtiene respuesta del teclado --------------------
TECLA PROC
	MOV AH,07H					; leer tecla sin eco
    INT 21H						
	RET
TECLA ENDP
; ------------------- Restaurar modo original de pantalla --------------------
RESTAURAR_MODO_ORIGINAL PROC
	MOV AL,03H				; modo texto 03h
	MOV AH,00H
	INT 10H
	RET
RESTAURAR_MODO_ORIGINAL ENDP

clean_screen PROC
    push ax
    push bx
    push cx
    push dx

    mov ah, 0Ah      ; función 0Ah de la interrupción 10h
    mov al, ' '      ; carácter a escribir (espacio en blanco)
    mov bh, 0        ; página de visualización
    mov cx, 0        ; posición inicial en la pantalla
    mov dx, 184Fh    ; posición final en la pantalla (80 columnas x 25 filas x 2 bytes por carácter)

    int 10h          ; llama a la interrupción 10h

    pop dx
    pop cx
    pop bx
    pop ax
    ret
clean_screen ENDP

END MAIN